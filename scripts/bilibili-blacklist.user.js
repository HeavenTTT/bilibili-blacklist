  let observerError = 0;
  function initObserver(container) {
    const rootNode =
      document.getElementById(container) ||
      document.querySelector(container) ||
      document.documentElement; // 回退到整个文档

    if (rootNode) {
      observer.observe(rootNode, {
        childList: true, // 监视添加/移除的节点
        subtree: true, // 监视所有后代
      });
      return true;
    } else {
      // 如果没找到根节点则重试
      setTimeout(() => initObserver(container), 500);
      console.warn("[bilibili-blacklist] 未找到根节点，正在重试...");
      observerError++;

      if (observerError > 10) {
        console.error("[bilibili-blacklist] 重试次数过多，停止重试。");
        return false;
      }
    }
  }
